#include "ft_printf_bonus.h"

int	ft_is_valid_type(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'd') || (c == 'i') || (c == 'f')
		|| (c == 'u') || (c == 'x') || (c == 'X') || (c == 'p') || (c == '%'));
}

int	ft_is_valid_flag(int c)
{
	return ((c == '-') || (c == ' ') || (c == '0') || (c == '.') || (c == '*')
		|| (c == '+') || (c == '#'));
}
